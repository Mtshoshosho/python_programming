# 日本語（Japanese）に変換する
# Pythonの基本(basic of Python)
# 変数の宣言は、変数名 = 値
# 型の宣言は不要
num = 1
print(num)
name = "Python"
print(name)
bool = True
print(bool)
# 型の確認はtype関数
print(type(num))
print(type(name))
print(type(bool))
# 型変換は、int(), str(), bool()
print(int("1"))
print(str(1))

print("Hello\nWorld") # 改行
print("Hello\tWorld") # タブ
print("Hello\"World") # ダブルクォーテーション
print("Hello\'World") # シングルクォーテーション
print("Hello\\World") # バックスラッシュ
